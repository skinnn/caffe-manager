<template>
  <v-layout row class="mt-5">
    <v-flex xs6 offset-xs3>
      <div class="white elevation-5">
        <v-toolbar class="black toolbar" flat dense>
          <v-toolbar-title class="toolbar-title">Register User</v-toolbar-title>
        </v-toolbar>
        <div class="register-page pl-4 pr-4 pb-3 pt-4">
          <router-link to="/admin/register"><v-btn color="blue">Admin Register</v-btn></router-link>
          <router-link to="/admin/login"><v-btn color="blue">Admin Login</v-btn></router-link>
          <router-link to="/user/register" event=""><v-btn color="blue">User Register</v-btn></router-link>
          <router-link to="/"><v-btn color="blue">User Login</v-btn></router-link>
          <form name="register-user-form" autocomplete="off">
            <v-text-field
              type="text"
              v-model="username"
              label="Username:"
              outline
            ></v-text-field>
            <v-text-field
              type="text"
              v-model="name"
              label="Full name:"
              outline
            ></v-text-field>
            <v-text-field
              type="password"
              v-model="password"
              label="Password:"
              outline
              autocomplete="new-password"
            ></v-text-field>
            <v-text-field
              type="password"
              v-model="password2"
              label="Confirm Password:"
              outline
            ></v-text-field>

            <h3 class="mt-4">Permissions</h3>
            <v-checkbox
              :label="`Warehouse - ${userMenu.warehouse.toString()}`"
              v-model="userMenu.warehouse"
            ></v-checkbox>
            <v-checkbox
              :label="`Tables - ${userMenu.tables.toString()}`"
              v-model="userMenu.tables"
            ></v-checkbox>
          </form>
          <!-- Display messages -->
          <div class="error-msg" v-if="error" v-html="error" />
          <div class="success-msg" v-if="success" v-html="success" />
          <br>
          <v-btn class="green register-button"
            block
            @click="registerUser">
            Register
          </v-btn>
        </div>

      </div>
    </v-flex>
  </v-layout>
</template>

<script>
import AuthenticationService from '@/services/AuthenticationService'

export default {
  data() {
    return {
      username: '',
      password: '',
      password2: '',
      name: '',
      error: null,
      success: null,
      userMenu: {
        home: true, // Default is that all users have home page
        warehouse: false,
        tables: false
      }
    }
  },
  methods: {
    async registerUser() {
      try {
        const response = (await AuthenticationService.registerUser({
          username: this.username,
          password: this.password,
          password2: this.password2,
          name: this.name,
          userMenu: {
            home: this.userMenu.home,
            warehouse: this.userMenu.warehouse,
            tables: this.userMenu.tables
          }
        })).data

        // If registering was successful redirect to user login page and print success msg
        if (response.user) {
          this.$router.push({
            name: 'user-login'
          })

          this.success = `User <span style="color: blue; font-size:17px;">${this.username}</span>
           registered successfully.`
          this.error = ''
        }
      } catch (error) {
        console.log(error)
        this.success = ''
        this.error = error.response.data.error
      }
    }
  }
}
</script>

